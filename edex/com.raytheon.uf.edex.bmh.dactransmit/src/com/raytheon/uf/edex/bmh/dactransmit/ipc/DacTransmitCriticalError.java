package com.raytheon.uf.edex.bmh.dactransmit.ipc;

import com.raytheon.uf.common.serialization.annotations.DynamicSerialize;
import com.raytheon.uf.common.serialization.annotations.DynamicSerializeElement;

/**
 * Notification to send to CommsManager indicating a critical error was
 * encountered that should be communicated back to the users.
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date         Ticket#    Engineer    Description
 * ------------ ---------- ----------- --------------------------
 * Aug 14, 2014  #3286     dgilling     Initial creation
 * 
 * </pre>
 * 
 * @author dgilling
 * @version 1.0
 */

@DynamicSerialize
public final class DacTransmitCriticalError {

    @DynamicSerializeElement
    private String errorMessage;

    @DynamicSerializeElement
    private Throwable throwable;

    public DacTransmitCriticalError() {

    }

    public DacTransmitCriticalError(String errorMessage, Throwable t) {
        this.errorMessage = errorMessage;
        this.throwable = t;
    }

    public String getErrorMessage() {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage) {
        this.errorMessage = errorMessage;
    }

    public Throwable getThrowable() {
        return throwable;
    }

    public void setThrowable(Throwable throwable) {
        this.throwable = throwable;
    }
}
