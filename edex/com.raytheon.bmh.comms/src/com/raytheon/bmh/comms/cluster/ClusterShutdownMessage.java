package com.raytheon.bmh.comms.cluster;

import com.raytheon.uf.common.serialization.annotations.DynamicSerialize;
import com.raytheon.uf.common.serialization.annotations.DynamicSerializeElement;

/**
 * 
 * Message used by comms managers to communicate to each other about shutting
 * down. When a comms manager is shutting down it should send a shut down
 * message with acknowledged=false. Any comms managers receiving that message
 * should respond with an acknowledge shutdown message where acknowledge=true.
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date          Ticket#  Engineer    Description
 * ------------- -------- ----------- --------------------------
 * Sep 24, 2014  3485     bsteffen    Initial creation
 * 
 * </pre>
 * 
 * @author bsteffen
 * @version 1.0
 */
@DynamicSerialize
public class ClusterShutdownMessage {

    @DynamicSerializeElement
    private boolean acknowledged;

    public ClusterShutdownMessage() {
        this(false);
    }

    public ClusterShutdownMessage(boolean acknowledged) {
        this.acknowledged = acknowledged;
    }

    public boolean isAcknowledged() {
        return acknowledged;
    }

    public void setAcknowledged(boolean acknowledged) {
        this.acknowledged = acknowledged;
    }

}
