package com.raytheon.uf.common.bmh.notify;

import com.raytheon.uf.common.serialization.annotations.DynamicSerialize;
import com.raytheon.uf.common.serialization.annotations.DynamicSerializeElement;

/**
 * Notification that no more messages available to play for a given group.
 * 
 * <pre>
 * 
 * SOFTWARE HISTORY
 * 
 * Date         Ticket#    Engineer    Description
 * ------------ ---------- ----------- --------------------------
 * Jun 1, 2015  4369       rferrel     Initial creation
 * Mar 14, 2016 5472       rjpeter     Added reason for NoPlaybackMessageNotificaiton.
 * </pre>
 * 
 * @author rferrel
 * @version 1.0
 */
@DynamicSerialize
public class NoPlaybackMessageNotification {
    @DynamicSerializeElement
    private String groupName;

    @DynamicSerializeElement
    private String reason;

    public NoPlaybackMessageNotification() {

    }

    public NoPlaybackMessageNotification(String groupName, String reason) {
        this.groupName = groupName;
        this.reason = reason;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    /**
     * @return the reason
     */
    public String getReason() {
        return reason;
    }

    /**
     * @param reason
     *            the reason to set
     */
    public void setReason(String reason) {
        this.reason = reason;
    }
}
